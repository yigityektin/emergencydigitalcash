#define RC522_CS_PORT GPIOA
#define RC522_CS_PIN  GPIO_PIN_4

#define RC522_RST_PORT GPIOB
#define RC522_RST_PIN  GPIO_PIN_0

static inline void RC522_CS_LOW(void){ HAL_GPIO_WritePin(RC522_CS_PORT, RC522_CS_PIN, GPIO_PIN_RESET); }
static inline void RC522_CS_HIGH(void){ HAL_GPIO_WritePin(RC522_CS_PORT, RC522_CS_PIN, GPIO_PIN_SET); }

static inline void RC522_RST_LOW(void){ HAL_GPIO_WritePin(RC522_RST_PORT, RC522_RST_PIN, GPIO_PIN_RESET); }
static inline void RC522_RST_HIGH(void){ HAL_GPIO_WritePin(RC522_RST_PORT, RC522_RST_PIN, GPIO_PIN_SET); }

static uint8_t rc522_read_reg(uint8_t reg){
  uint8_t addr = ((reg << 1) & 0x7E) | 0x80; // read
  uint8_t val  = 0;
  RC522_CS_LOW();
  HAL_SPI_Transmit(&hspi1, &addr, 1, 100);
  HAL_SPI_Receive(&hspi1, &val, 1, 100);
  RC522_CS_HIGH();
  return val;
}

